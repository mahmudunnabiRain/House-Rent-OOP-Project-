    public ImageIcon resizeImageIcon(String imagePath){
        // This method resize & return imageicon from selected path
        
        ImageIcon rawImage = new ImageIcon(imagePath);
        Image img1 = rawImage.getImage();
        Image img2 = img1.getScaledInstance(imageLabelOne.getWidth(), imageLabelOne.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon newImage = new ImageIcon(img2);
        return newImage;
    }
    
    public ImageIcon blobToImageIcon(Blob blob) {
        
        ImageIcon newImage = null;
        
        try {           
            
            InputStream is = blob.getBinaryStream();
            BufferedImage img1 = ImageIO.read(is);
            Image img2 = img1.getScaledInstance(imageLabelOne.getWidth(), imageLabelOne.getHeight(), Image.SCALE_SMOOTH);
            newImage = new ImageIcon(img2);
            
        } catch (IOException ex) {
            Logger.getLogger(EditAdPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(EditAdPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return newImage;
        
    }
    
    
