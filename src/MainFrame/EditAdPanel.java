    private void downloadData(){
        Connection myConnection = null;
        Statement myStatement  = null;
        ResultSet myResultSet = null;
        ResultSetMetaData myMetaData = null;
        
        
        String sql = "select * from houseAd";
        
        try{
            // get connection to database
            myConnection = DriverManager.getConnection("jdbc:derby://localhost:1527/RentHouseDatabase", "rain013", "oopfinalproject");
            
            // creaTe statement
            myStatement = myConnection.createStatement();
            
            myResultSet = myStatement.executeQuery(sql);
            
            myMetaData = myResultSet.getMetaData();
            
            int columnCount = myMetaData.getColumnCount();
            
            targetHouseId = targetHouseIdField.getText();
            targetPassword = targetPasswordField.getText();
            
            boolean found = false;
            
            while(myResultSet.next())
            {
                if(targetHouseId.equals(myResultSet.getString("houseId")) && targetPassword.equals(myResultSet.getString("password"))){
                    houseIdField.setText(myResultSet.getString("houseId"));
                    titleField.setText(myResultSet.getString("title"));
                    areaField.setText(myResultSet.getString("area"));
                    cityField.setText(myResultSet.getString("city"));
                    streetField.setText(myResultSet.getString("street"));
                    postcodeField.setText(myResultSet.getString("postcode"));
                    roomsComboBox.setSelectedIndex(myResultSet.getInt("rooms"));
                    belconyComboBox.setSelectedIndex(myResultSet.getInt("belcony"));
                    bathroomsComboBox.setSelectedIndex(myResultSet.getInt("bathrooms"));
                    floorNoComboBox.setSelectedIndex(myResultSet.getInt("floorNo"));
                    descriptionField.setText(myResultSet.getString("description"));
                    monthComboBox.setSelectedItem(myResultSet.getString("month"));
                    priceField.setText(myResultSet.getDouble("price")+"");
                    ownerNameField.setText(myResultSet.getString("ownerName"));
                    emailField.setText(myResultSet.getString("email"));
                    passwordField.setText(myResultSet.getString("password"));
                                        
                    blobOne = myResultSet.getBlob("imageOne");
                    if(blobOne != null)
                        imageLabelOne.setIcon(blobToImageIcon(blobOne));                   

                    
                    blobTwo = myResultSet.getBlob("imageTwo");
                    if(blobTwo != null)
                        imageLabelTwo.setIcon(blobToImageIcon(blobTwo));

                    
                    blobThree = myResultSet.getBlob("imageThree");
                    if(blobThree != null)
                        imageLabelThree.setIcon(blobToImageIcon(blobThree));
                    
                    
                    
                    contactNoField.setText(myResultSet.getString("contactNo"));
                    
                    adType = myResultSet.getString("adType");
                    if(adType.equals("sale")){
                        forSaleRadioButton.setSelected(true);
                    }
                    if(adType.equals("rent")){
                        forSaleRadioButton.setSelected(true);
                    }
                    
                    customerType = myResultSet.getString("customerType");
                    if(customerType.equals("bachelor")){
                        bachelorRadioButton.setSelected(true);
                    }
                    if(customerType.equals("family")){
                        familyRadioButton.setSelected(true);
                    }
                    if(customerType.equals("both")){
                        familyRadioButton.setSelected(true);
                    }
                    
                    found = true;
                    
                    
                }
            }
            
            if(found == true){
                importPanel.setVisible(false);
            }
            
            if(found == false){
                importErrorLabel.setText("wrong username or password !");
            }
            
           
        }
        catch(SQLException e){
            e.printStackTrace();
        }
        
    }
    
